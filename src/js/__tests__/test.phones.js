import numberFormatting from '../phones';

test.each([
  [+7905],
  ['+7905'],
  ['+7(905)'],
  ['+7(405)548-88'],
  ['имя'],
  [null],
  [undefined],
])('%p', (phoneNumber) => {
  expect(() => {
    numberFormatting(phoneNumber);
  }).toThrow();
});

test.each([
  ['+7(123)456-78-90', '+71234567890'],
  ['+8(123)456-78-90', '+71234567890'],
  ['8(123)456-78-90', '+71234567890'],
  ['9(123)456-78-90', '+91234567890'],
  ['(123)456-78-90', '+71234567890'],
  ['(123)456-78-90-123', '+1234567890123'],
  ['(12dds3)456ds-78-9ds0', '+71234567890'],
  [71234567890, '+71234567890'],
  [81234567890, '+71234567890'],
  [1234567890, '+71234567890'],
  [1234567890123, '+1234567890123'],
])('%p', (phoneNumber, expected) => {
  const result = numberFormatting(phoneNumber);
  expect(result).toBe(expected);
});
